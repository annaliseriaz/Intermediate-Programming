package math;

public class Arithmetic {
		public int addition(int num1, int num2) {
			int sum;
			sum = num1 + num2;
			return sum;
		}
		
		public int subtract(int num1, int num2) {
			int diff;
			diff = num1 - num2;
			return diff;
		}
		
		public int multiply(int num1, int num2) {
			int mult;
			mult = num1 * num2;
			return mult;
		}
	}
